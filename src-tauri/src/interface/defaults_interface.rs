// Dependencies
use serde_json::json;

/* ------------------------------------------------------------------------------------------------------------------ */

pub fn defaults_interface() -> std::collections::HashMap<String, serde_json::Value> {
  let mut defaults = std::collections::HashMap::new();
  defaults.insert("interface".to_string(), json!({
    "selectedPage": "home"
  }));
  defaults
}

