// Rust
use std::collections::HashMap;
// Dependencies
use serde_json::json;
use serde_json::Value;

/* ---------------------------------------------------------------------------------------------- */

pub fn state_default() -> HashMap<String, Value> {
  let mut defaults = HashMap::new();

  // app
  defaults.insert(
    "State".to_string(),
    json!({
      "tauri_ready": false,
      "react_ready": false
    }),
  );

  defaults
}

