pub fn default_app_state() -> HashMap<String, Value> {
  let mut defaults = HashMap::new();

  // app
  defaults.insert(
    "Setup".to_string(),
    json!({
      "tauri_ready": false,
      "react_ready": false
    })
  );

  defaults
}

